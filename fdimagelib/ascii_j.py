def asciij_to_utf8(asciij_str:bytearray) -> str:
    res = ''
    for ch in asciij_str:
        if ch == ord('\r') or ch == ord('\n'):
            utf8 = chr(ch)
        else:
            utf8 = ascii_table_half[ch]
        res += utf8
    return res

